int	ft_strcmp(char *s1, char *s2)
{
	int	r;
	int	i;

	r = 0;
	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] == s2[i])
			i++;
		else
		{
			r = s1[i] - s2[i];
			break ;
		}
	}
	if (r == 0)
		r = s1[i] - s2[i];
	if (r == 0)
		return (0);
	else if (r > 0)
		return (1);
	else
		return (-1);
}
