#include <unistd.h>

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
	ft_putchar(10);
}

int	main(int argc, char **argv)
{
	int	i;

	argc = argc;
	i = 1;
	while (argv[i] != 0)
	{
		ft_putstr(argv[i]);
		i++;
	}
}
